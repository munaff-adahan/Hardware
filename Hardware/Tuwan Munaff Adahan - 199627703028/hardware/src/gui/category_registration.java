    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText().toUpperCase();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Category", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            if (jButton1.getText().equals("Update")) {
                try {
                    ResultSet rs = MySQL.search("SELECT * FROM `category` WHERE `name`='" + name + "' AND `id`<>" + catId + "");

                    if (rs.next()) {
                        JOptionPane.showMessageDialog(this, "Category Already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {

                        MySQL.iud("UPDATE `category` SET `name`='" + name + "' WHERE `id`=" + catId + "");

                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
                jButton1.setText("Save");

            } else {

                try {

                    ResultSet rs = MySQL.search("SELECT * FROM `category` WHERE `name`='" + name + "'");

                    if (rs.next()) {
                        JOptionPane.showMessageDialog(this, "Category Already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {

                        MySQL.iud("INSERT INTO `category`(`name`) VALUES('" + name + "')");
                        JOptionPane.showMessageDialog(this, "Category Added Successully", "Success", JOptionPane.INFORMATION_MESSAGE);

                    }

                } catch (Exception e) {
                    e.printStackTrace();
                }

            }
            jTextField1.setText("");
            loadCategories();
            this.pr.loadCategory();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {

            r = jTable1.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please Select a Category", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {

                int option = JOptionPane.showConfirmDialog(this, "Do you want Modify Category ?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

                if (option == JOptionPane.YES_OPTION) {

                    jTextField1.setText(jTable1.getValueAt(r, 1).toString());
                    jButton1.setText("Update");
                    catId = jTable1.getValueAt(r, 0).toString();

                }

            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

