    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int r = jTable1.getSelectedRow();
            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please Select a company", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {

                if (grn2 != null) {
                    String pid = jTable1.getValueAt(r, 0).toString();

                    String pname = jTable1.getValueAt(r, 1).toString();
                    String brand = jTable1.getValueAt(r, 2).toString();
                    String catogary = jTable1.getValueAt(r, 3).toString();

                    grn2.jLabel13.setText(pid);
                    grn2.jLabel12.setText(pname);
                    grn2.jLabel16.setText(brand);
                    grn2.jLabel19.setText(catogary);
                    grn2.jTextField1.setText("");
                    grn2.jTextField2.setText("");
                    grn2.jTextField1.grabFocus();

                    this.dispose();
                }

            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

